package numberSystems;

public class NumberSystem 
{

	public static void main(String[] args)
	{
		int a = 0b1010 ;     //0b ke baad binary number denge ---- decimal wala 10 save hoga
		int b = 012 ;        // 0 ke baad octal   --- decimal wala 10 save hoga
		int c = 0xA ;        // 0x ke baad hexadecimal number
		
		System.out.println(a + " " + b + " " + c);
	}

}
