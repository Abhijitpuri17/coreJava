package loops;

public class ContinueInForLoop {
                                                        // statement after continue in loop
	                                                    // are not executed and loop is again
	                                                    // started for new value of i
	public static void main(String[] args) {
		
		// enter the numbers from 1 to 100 excluding (40 , 50)
		
		for (int i = 1 ; i <= 100 ; i++) {
			if (i > 40 && i < 50) continue ;
			System.out.println(i);
		}
    	 
    	 }
    	 }
		

    	 
    	 
    	 
    	 

	